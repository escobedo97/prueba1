package holamundo;

public class EvaluarExpresiones {
	public static void main(String[] args) {
		
		
		int num1 = 34;
		int num2 = 5;
		
		System.out.println("La suma  Numero 1 + Numero 2 es: " + num1 + " + " + num2 + " = " + (num1+num2) );
		System.out.println("La resta  Numero 1 – Numero 2 es: " + num1 + " - " + num2 + " = " + (num1-num2));
		System.out.println("El producto  Numero 1 * Numero 2 es: " + num1 + " * " + num2 + " = " + (num1*num2));
		System.out.println("El cociente  Numero 1 / Numero 2 es: " + num1 + " / " + num2 + " = " + (num1/num2));
		System.out.println("El resto  Numero 1 % Numero 2 es: " + num1 + " % " + num2 + " = " + (num1%num2));

	}

}
